package Variaveis_e_constantes;

public class TiposPrimitivos {

	public static void main(String[] args) {
		String variavelString = ""; //O tipo String não possui um tipo primitivo equivalente
		
		byte variavelByte = 127;// O tipo byte, assim como outros primitivos, não pode manter
								  //um estado de ausencia de valor e representado pela palavra null
								  // que significa nulo.
	
		short variavelShort = 1;
		
		int variavelInteger = 1; //Tipo normal de int e Integer
		
		long variavelLong = 1L;
		
		float variavelFloat = 1.0f;

        double variavelDouble = 1.0;

        char variavelCharacter = 'A'; // o tipo normal de char [e Character

        boolean variavelBoolean = true;
	
	
	
	}

}
